public void cbk(){
        int cod = ckbGrupo.getSelectedIndex();
        
      String preencher;
      
        preencher = "select descricao from exercicios where grupo_ex =" + cod;
        if(cod == 0){
             preencher = "select descricao from exercicios where " ;
        }
        try{
            ResultSet rs = bd.executarconsulta(preencher);
            ResultSetMetaData rsmd = rs.getMetaData();
            int num_coluna = rsmd.getColumnCount();
            DefaultTableModel pesq_pag = new DefaultTableModel();
           this.tblExercicio.setModel(pesq_pag);
           
            
                 for(int x=1; x <= num_coluna ; x++){
                pesq_pag.addColumn(rsmd.getColumnLabel(x));
                }
            
                 while(rs.next()){

                    Object [] file = new Object[num_coluna];
                
                     for(int y=0 ; y < num_coluna ; y++)
                    {
                      file[y] = rs.getObject(y+1);
                      ArrayList list = new ArrayList();
                  list.add(rs.getObject(y+1));
                  ckbExercicio.addItem(list);
                    }
                    pesq_pag.addRow(file);
                  
                 }
                 
        }
                 catch(SQLException ex) {
            System.out.println("Erro na consulta");
            System.out.println(preencher);
            
                 }
       }
